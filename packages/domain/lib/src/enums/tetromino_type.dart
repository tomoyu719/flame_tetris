/// テトリミノの種類を表す列挙型
///
/// 標準テトリスの7種類のテトリミノを定義
/// 各テトリミノには固有の色（RGB値）が関連付けられている
enum TetrominoType {
  /// I-テトリミノ（水色）- 4×1の棒状
  i(0x00, 0xF0, 0xF0),

  /// O-テトリミノ（黄色）- 2×2の正方形
  o(0xF0, 0xF0, 0x00),

  /// T-テトリミノ（紫）- T字型
  t(0xA0, 0x00, 0xF0),

  /// S-テトリミノ（緑）- S字型
  s(0x00, 0xF0, 0x00),

  /// Z-テトリミノ（赤）- Z字型
  z(0xF0, 0x00, 0x00),

  /// J-テトリミノ（青）- J字型
  j(0x00, 0x00, 0xF0),

  /// L-テトリミノ（オレンジ）- L字型
  l(0xF0, 0xA0, 0x00);

  const TetrominoType(this.r, this.g, this.b);

  /// 赤成分（0-255）
  final int r;

  /// 緑成分（0-255）
  final int g;

  /// 青成分（0-255）
  final int b;

  /// ARGB形式の色値を取得（アルファ値は0xFF固定）
  int get colorValue => (0xFF << 24) | (r << 16) | (g << 8) | b;
}
